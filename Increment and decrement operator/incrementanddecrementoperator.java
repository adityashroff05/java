public class incrementanddecrementoperator {
    public static void main(String[] args) {
        int x = 5;
        //x++; x++ -> x+1
        System.out.println(x++);
        //x--; x-- -> x-1
        System.out.println(x--);
        ++x; //++x -> this is pre increment
        System.out.println(x);
        --x; 
        System.out.println(x); 
    }
}
